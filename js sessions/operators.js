// `age` stores a numeric value.
// `var` declares a variable (function-scoped in JavaScript).
var age = 4;

// Ternary operator checks a condition and returns one of two values:
// condition ? value_if_true : value_if_false
// `age >= 18` uses the comparison operator `>=` (greater than or equal to).
// If true -> "Eligible", else -> "Ineligible".
var result = age >= 18 ? "Eligible" : "Ineligible";

// Print the final text result to the console.
console.log(result);
